<h1>Help</h1>
<h2>Registering/managing an account</h2>
<p>Click the <em>register</em> menu item to create a new account. You will need an email account for the purpose of resetting your password if you forget it.</p>
<p>To look at your account, log in to your newly created account, click the <em>account</em> menu item. There you should be able to see which teams you are a participant of, which tournaments you are involved with, and the ability to edit your personal details.</p>
<h2>Signing up for a sport</h2>
<p>To sign up for a tournament, you must first have an account. See <em>registering/managing an account</em> for information how to do that.</p>
<p>When signing up, you will be requested to enter in your personal details, including your address, phone number, emergency contact details. This information is only used for emergency situations.</p>
<p>If you are signing up as a team, one person has to enter in all the details about each participant.</p>
<p>To complete the sign up, press "Complete Sign up Process". This should add you or your team to the tournament. If you wish to not participate a tournament, please contact support at Riccarton Sports Centre.</p>